package day06_PrimitiveTypesCastings;

public class ArithmeticOperators {


    public static void main(String[] args) {


        System.out.println(10/4); // only integer part nor 2.5
        System.out.println(100/3);
        System.out.println(50/2);
        System.out.println(10+4);
        System.out.println(5*3);
        System.out.println(10%3); // remainder
        System.out.println((double)10/4); // double is decimal
        System.out.println(10/4.0); // when use decimal number result will be double
        System.out.println(10d/4); // when write d it will be decimal

        int a = 100;
        double b = a/6; // integer/integer result is integer 16.0
        System.out.println(b);








    }
}
